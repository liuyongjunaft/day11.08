package com.exam.user.common;

public enum ResultStatus {
    SUSSCES(20000,"sussces"),
    ERROR(40000,"error");
    private int status;

    ResultStatus(int status, String msg) {
        this.status = status;
        this.msg = msg;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    private String msg;
}
